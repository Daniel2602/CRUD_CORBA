    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        Mascota mas = new Mascota ();
        int identificacion = Integer.parseInt(txtId.getText());
        String lista = mas.consultarMascota(identificacion);
        JOptionPane.showMessageDialog(null, lista);
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Mascota mas = new Mascota();
        boolean resultado = mas.actualizarMascota (Integer.parseInt(txtId.getText()) , txtNombre.getText(), txtApellido.getText(), txtNombreDueno.getText(),(Integer.parseInt(txtTelefono.getText())));
        if (resultado==true) {
            JOptionPane.showMessageDialog(null, "Los datos se actualizaron");
        }else{
            JOptionPane.showMessageDialog(null, "Los datos no se actualizaron");
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        Mascota mas = new Mascota();
        boolean resultado = mas.eliminarMascota(Integer.parseInt(txtId.getText()));
        if (resultado==true) {
            JOptionPane.showMessageDialog(null, "Los datos se eliminaron");
        }else{
            JOptionPane.showMessageDialog(null, "Los datos no se eliminaron");
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

